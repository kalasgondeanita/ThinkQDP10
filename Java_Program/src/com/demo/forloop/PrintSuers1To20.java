package com.demo.forloop;

public class PrintSuers1To20 {
	public static void PrintSquers() {
		int i;
		int n=0;
		for(i=1;i<=20;i++) {
		n=i*i;
		System.out.println(n);
	}
	}

	public static void main(String[] args) {
		PrintSquers();

	}

}
